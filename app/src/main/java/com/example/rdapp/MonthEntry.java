package com.example.rdapp;

class MonthEntry {

    public String Name, Amount, AccNo, Date;
    public int Month1;

    public MonthEntry() {
    }

    public MonthEntry(String name, String amount, String accNo, String date, int month1) {
        Name = name;
        Amount = amount;
        AccNo = accNo;
        Date = date;
        Month1 = month1;
    }


}
